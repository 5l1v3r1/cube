/**
 * This class provides heuristic information about a 3x3x3 cuboid.
 */
class Heuristic3x3x3 {
  /**
   * The edges of the 3x3x3 cubiod.
   */
  final Edges edges;
  
  /**
   * Create a [Heuristic3x3x3] from an edge map.
   */
  Heuristic3x3x3(this.edges) {
    assert(edges.size == 3);
  }
  
  /**
   * Generates a unique hash from 0 to 2047 (inclusive) which represents the
   * edge orientation case on the cube.
   * 
   * The [axis] argument specifiecs the edge orientation scheme. In the desired
   * scheme, an edge is good if it can be solved without turning the two faces
   * perpendicular to [axis]. The axis is identified by a number 0 through 2 for
   * the axis x, y, and z respectively. The ZZ method uses an [axis] of 2.
   */
  int hashEO(int axis) {
    int res = 0;
    for (int i = 0; i < 11; ++i) {
      bool oriented = edges.isOriented(i, 0, axis);
      if (oriented) {
        res |= 1 << i;
      }
    }
    return res;
  }
  
  /**
   * Generate a unique hash of the state of the FD and BD edges. This ranges
   * between 0 and 527 inclusive.
   */
  int hashEOLineEdges() {
    int frontPos = -1;
    int backPos = -1;
    for (int i = 0; i < 12; ++i) {
      int edge = edges.readEdge(i, 0);
      if (edge == 2) frontPos = i;
      else if (edge == 8) backPos = i;
    }
    assert(frontPos >= 0 && backPos >= 0);
    int orientations = 0;
    if (edges.isOriented(frontPos, 0, 2)) {
      orientations |= 1;
    }
    if (edges.isOriented(backPos, 0, 2)) {
      orientations |= 2;
    }
    if (backPos > frontPos) --backPos;
    return backPos + frontPos * 11 + 132 * orientations;
  }
  
  /**
   * Hash the edge orientations and the position of an edge.
   */
  int hashEOAndEdge(int axis, int edge) {
    int result = hashEO(axis);
    for (int i = 0; i < 12; ++i) {
      if (edges.readEdge(i, 0) == edge) {
        return result + i * 2048;
      }
    }
    throw new StateError('missing edge: $edge');
  }
  
  /**
   * Returns the number of moves required to orient every edge with respect to
   * an [axis].
   */
  int edgeOrientationMoveCount(int axis) {
    int hash = hashEO(axis);
    assert(axis >= 0 && axis < 3);
    if (axis == 0) return xEOHeuristic[hash];
    else if (axis == 1) return yEOHeuristic[hash];
    else return zEOHeuristic[hash];
  }
  
  /**
   * Returns the number of moves required to solve the line edges.
   */
  int eoLineEdgeMoveCount() {
    return eoLineHeuristic[hashEOLineEdges()];
  }
  
  int eoLineFrontEdgeMoveCount() {
    return eoFrontEdgeHeuristic[hashEOAndEdge(2, 2)];
  }
  
